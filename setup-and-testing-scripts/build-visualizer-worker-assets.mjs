import { mkdir, readFile, readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const distDir = path.join(repoRoot, "visualizer-app", "dist");
const outputPath = path.join(repoRoot, "cloudflare-worker", "visualizer-assets.generated.mjs");

const html = await readFile(path.join(distDir, "index.html"), "utf8");
const assets = {};

await collectAssets(distDir);
await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(
  outputPath,
  [
    "// Generated by `npm run visualizer:build`. Do not edit by hand.",
    `export const visualizerHtml = ${JSON.stringify(html)};`,
    `export const visualizerAssets = ${JSON.stringify(assets, null, 2)};`,
    "",
  ].join("\n")
);

console.log(
  JSON.stringify({
    ok: true,
    output_path: path.relative(repoRoot, outputPath),
    asset_count: Object.keys(assets).length,
  })
);

async function collectAssets(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      await collectAssets(fullPath);
      continue;
    }

    if (entry.name === "index.html") continue;
    const relative = path.relative(distDir, fullPath).split(path.sep).join("/");
    const pathname = `/visualizer/${relative}`;
    const body = await readFile(fullPath, "utf8");
    assets[pathname] = {
      content_type: contentTypeFor(relative),
      body,
    };
  }
}

function contentTypeFor(filePath) {
  const extension = path.extname(filePath).toLowerCase();
  if (extension === ".js" || extension === ".mjs") return "text/javascript; charset=utf-8";
  if (extension === ".css") return "text/css; charset=utf-8";
  if (extension === ".svg") return "image/svg+xml";
  if (extension === ".json") return "application/json; charset=utf-8";
  if (extension === ".ico") return "image/x-icon";
  return "application/octet-stream";
}
